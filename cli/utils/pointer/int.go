package pointer

// MakeIntPointer returns an int pointer of the given argument
func MakeIntPointer(i int) *int {
	return &i
}

// MakeIntPointer returns an int8 pointer of the given argument
func MakeInt8Pointer(i int8) *int8 {
	return &i
}

// MakeIntPointer returns an int16 pointer of the given argument
func MakeInt16Pointer(i int16) *int16 {
	return &i
}

// MakeIntPointer returns an int32 pointer of the given argument
func MakeInt32Pointer(i int32) *int32 {
	return &i
}

// MakeIntPointer returns an int64 pointer of the given argument
func MakeInt64Pointer(i int64) *int64 {
	return &i
}
